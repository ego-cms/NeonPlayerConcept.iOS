import UIKit


extension UIFont {
    static func openSansRegular(_ size: CGFloat) -> UIFont {
        return UIFont(name: "OpenSans", size: size)!
    }
//
    static func varelaRound(_ size: CGFloat) -> UIFont {
        return UIFont(name: "VarelaRound", size: size)!
    }
}
